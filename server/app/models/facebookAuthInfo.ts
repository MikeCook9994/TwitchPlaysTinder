export default interface FacebookAuthInfo {
    
    /**
     * the email address of the facebook account to authenticate with
     * 
     * @type {string}
     * @memberof FacebookAuthInfo
     */
    email: string;

    /**
     * the password of the associated facebook account
     * 
     * @type {string}
     * @memberof FacebookAuthInfo
     */
    password: string;
}